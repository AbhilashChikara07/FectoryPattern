package com.example.chikara.factorypattern.CreationalPattern.BuilderPattern;

public class BuilderEntity {

    String carSeatBalt;
    String carStaring;
    String carBreak;

    public String getCarSeatBalt() {
        return carSeatBalt;
    }

    public void setCarSeatBalt(String carSeatBalt) {
        this.carSeatBalt = carSeatBalt;
    }

    public String getCarStaring() {
        return carStaring;
    }

    public void setCarStaring(String carStaring) {
        this.carStaring = carStaring;
    }

    public String getCarBreak() {
        return carBreak;
    }

    public void setCarBreak(String carBreak) {
        this.carBreak = carBreak;
    }

}
